public class Personas {
    private String nombre;
    private String calb;
    private String calm;
    private String total;
    private String ultcal;

    public void setUltcal(String ultcal) {
        this.ultcal = ultcal;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setCalb(String calb) {
        this.calb = calb;
    }

    public void setCalm(String calm) {
        this.calm = calm;
    }

    public void setTotal(String total) {
        this.total = total;
    }



    public String getUltcal() {
        return ultcal;
    }

    public String getNombre() {
        return nombre;
    }

    public String getCalb() {
        return calb;
    }

    public String getCalm() {
        return calm;
    }

    public String getTotal() {
        return total;
    }



}
